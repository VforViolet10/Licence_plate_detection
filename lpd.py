import os
from tensorflow.keras.preprocessing.image import ImageDataGenerator
from tensorflow.keras.applications import MobileNetV2
from tensorflow.keras.layers import GlobalAveragePooling2D, Dense, Dropout, Input
from tensorflow.keras.models import Model
from tensorflow.keras.optimizers import Adam
from tensorflow.keras.callbacks import ModelCheckpoint, ReduceLROnPlateau

# ======================================
# CONFIG
# ======================================
DATA_DIR = "dataset"
IMG_SIZE = (224, 224)
BATCH_SIZE = 32
EPOCHS = 10
MODEL_SAVE = "plate_classifier_mobilenetv2.h5"

# ======================================
# DATA GENERATORS
# ======================================
train_datagen = ImageDataGenerator(
    rescale=1./255,
    rotation_range=8,
    width_shift_range=0.08,
    height_shift_range=0.08,
    shear_range=0.08,
    zoom_range=0.08,
    horizontal_flip=True,
    fill_mode="nearest"
)

val_datagen = ImageDataGenerator(rescale=1./255)

train_gen = train_datagen.flow_from_directory(
    os.path.join(DATA_DIR, "train"),
    target_size=IMG_SIZE,
    batch_size=BATCH_SIZE,
    class_mode="binary"
)

val_gen = val_datagen.flow_from_directory(
    os.path.join(DATA_DIR, "val"),
    target_size=IMG_SIZE,
    batch_size=BATCH_SIZE,
    class_mode="binary"
)

# ======================================
# MODEL
# ======================================
base = MobileNetV2(input_shape=(IMG_SIZE[0], IMG_SIZE[1], 3), include_top=False, weights="imagenet")
x = base.output
x = GlobalAveragePooling2D()(x)
x = Dropout(0.3)(x)
pred = Dense(1, activation="sigmoid")(x)

model = Model(inputs=base.input, outputs=pred)

for layer in base.layers:
    layer.trainable = False

model.compile(optimizer=Adam(1e-3), loss="binary_crossentropy", metrics=["accuracy"])

callbacks = [
    ModelCheckpoint(MODEL_SAVE, save_best_only=True, monitor="val_accuracy", mode="max", verbose=1),
    ReduceLROnPlateau(monitor="val_loss", factor=0.5, patience=3, verbose=1)
]

print("ðŸš€ Training started...")
history = model.fit(
    train_gen,
    validation_data=val_gen,
    epochs=EPOCHS,
    callbacks=callbacks
)

print("\nâœ… Model saved as:", MODEL_SAVE)

